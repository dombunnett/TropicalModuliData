def sortTri(z,L):
	#sorts triangulations into folder based on which isomorphism class their dual graph is contained in.
	zReps=open("zReps.txt", 'r')
	for line in zReps:
		strippedLine=line.rstrip('\n')
		num = './g4T2Buckets/numG_'+strippedLine+'.txt'
		tri = './g4T2Buckets/triG_'+strippedLine+'.txt'
		numFile=open(num, 'r')
		triFile=open(tri, 'ab')
		for x in numFile:
			strippedx=x.rstrip('\n')
			intStrippedx=int(strippedx)
			if intStrippedx==z:
				triFile.write(str(L)+'\n')
				break
		numFile.close()
		triFile.close()

